
class BlogtoEvent {
    year: number;
    month: number;
    day: number;
    title: string;
    description: string;
    time:string;
    location: string;

    constructor(year: number, month: number, day: number, title: string, description: string, time: string, location: string){
            this.year = year;
            this.month = month;
            this.day = day;
            this.title = title;
            this.description = description;
            this.time = time;
            this.location = location;
    }

}

